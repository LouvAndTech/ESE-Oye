package fr.eseoye.eseoye.beans;

public class SimplifiedUser {

	private String name, surname;
	
	public SimplifiedUser(String name, String surname) {
		this.name = name;
		this.surname = surname;
	}
	
	public String getName() {
		return name;
	}
	
	public String getSurname() {
		return surname;
	}
	
}
